#include <stdio.h>

int	ft_strcmp(char *s1, char *s2)
{
	int	i;

	i = 0;
	while(s1[i] == s2[i])
	{
		if (s1[i] == '\0' && s2[i] == '\0')
			return (0);
		i++;
	}
	return ((unsigned char)s1[i] - (unsigned char)s2[i]);
}


int main(void)
{
	char s1[12] = "hello";
	char s2[] = "Bella";
	printf("%d", ft_strcmp(s1, s2));
	return (0);
}
